namespace Qube7.Composite.Design.Wizards
{
    /// <summary>
    /// Contains the replacement parameter keys.
    /// </summary>
    internal static class Constants
    {
        #region Fields

        /// <summary>
        /// Specifies the replacement parameter of the item name with all unsafe characters replaced.
        /// </summary>
        internal const string SafeName = "$safename$";

        /// <summary>
        /// Specifies the replacement parameter of the controller item name with all unsafe characters replaced.
        /// </summary>
        internal const string SafeController = "$safecontroller$";

        /// <summary>
        /// Specifies the replacement parameter of the view model item name with all unsafe characters replaced.
        /// </summary>
        internal const string SafeViewModel = "$safeviewmodel$";

        /// <summary>
        /// Specifies the replacement parameter of the view item name with all unsafe characters replaced.
        /// </summary>
        internal const string SafeView = "$safeview$";

        /// <summary>
        /// Specifies the replacement parameter of the item name provided by the user.
        /// </summary>
        internal const string Name = "$name$";

        /// <summary>
        /// Specifies the replacement parameter of the controller item name provided by the user.
        /// </summary>
        internal const string Controller = "$controller$";

        /// <summary>
        /// Specifies the replacement parameter of the view model item name provided by the user.
        /// </summary>
        internal const string ViewModel = "$view$";

        /// <summary>
        /// Specifies the replacement parameter of the view item name provided by the user.
        /// </summary>
        internal const string View = "$view$";

        #endregion
    }
}
